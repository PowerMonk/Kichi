import nodemailer from "nodemailer";

export interface EmailDispatchEntry {
  name: string;
  email: string;
  controlNumber: string;
  qrFilePath: string;
}

export interface EmailDispatchResult {
  enabled: boolean;
  sentCount: number;
  failed: Array<{ email: string; reason: string }>;
}

function getSmtpConfig() {
  const host = process.env.SMTP_HOST?.trim();
  const port = Number(process.env.SMTP_PORT ?? "587");
  const user = process.env.SMTP_USER?.trim();
  const pass = process.env.SMTP_PASS?.trim();
  const from = process.env.SMTP_FROM?.trim();
  const secure = (process.env.SMTP_SECURE ?? "false").toLowerCase() === "true";

  if (!host || !user || !pass || !from) {
    return null;
  }

  return {
    host,
    port,
    user,
    pass,
    from,
    secure,
  };
}

export async function sendSequentialEmails(
  entries: EmailDispatchEntry[],
  logFn: (eventName: string, details?: Record<string, unknown>) => void,
): Promise<EmailDispatchResult> {
  const smtp = getSmtpConfig();

  if (!smtp) {
    return {
      enabled: false,
      sentCount: 0,
      failed: [],
    };
  }

  const transporter = nodemailer.createTransport({
    host: smtp.host,
    port: smtp.port,
    secure: smtp.secure,
    auth: {
      user: smtp.user,
      pass: smtp.pass,
    },
  });

  let sentCount = 0;
  const failed: Array<{ email: string; reason: string }> = [];

  for (const entry of entries) {
    try {
      await transporter.sendMail({
        from: smtp.from,
        to: entry.email,
        subject: "Your Kichi QR Code",
        text: `Hello ${entry.name},\n\nAttached is your QR code for event access.\nControl number: ${entry.controlNumber}\n\nGenerated by Kichi.`,
        attachments: [
          {
            filename: `${entry.controlNumber}.png`,
            path: entry.qrFilePath,
            contentType: "image/png",
          },
        ],
      });

      sentCount += 1;
      logFn("Email sent", {
        email: entry.email,
        controlNumber: entry.controlNumber,
      });
    } catch (error) {
      const reason =
        error instanceof Error ? error.message : "Unknown SMTP error";
      failed.push({ email: entry.email, reason });
      logFn("Email failed", {
        email: entry.email,
        controlNumber: entry.controlNumber,
        reason,
      });
    }
  }

  return {
    enabled: true,
    sentCount,
    failed,
  };
}
